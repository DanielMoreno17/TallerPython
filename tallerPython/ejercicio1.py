
#las instruccion type(), nos ayuda a ver de que tipo de dato de una variable

#la siguiente instruccion devuelve el tipo str
introduccion = "hola esta es un ejemplo de cadena"
print(type(introduccion))

#la siguiente instruccion devuelve el tipo int
introduccion = 21
print(type(introduccion))

#La siguiente instruccion devuelve el tipo float
introduccion = 21.5
print(type(introduccion))

